package com.capgemini.krk.TO.profile;

import lombok.*;

public class ProfileTO {
    private int id;
    private String name;

    public ProfileTO(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}