package com.capgemini.krk.TO.mode;

import lombok.*;

public class ModeTO {
    private int id;
    private String name;

    public ModeTO(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
